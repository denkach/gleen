'use client';

import Image from 'next/image';
import { useCallback, useEffect, useState } from 'react';
import type { TimestampsArtifact } from '@/lib/analysis-pipeline/artifact-schemas';
import type { ResultSaveState } from '@/lib/result-workspace/actions';
import {
  formatKeyMomentsCount,
  formatResultCopy,
  type ResultCopy,
} from '@/lib/result-workspace/copy';

import { AutosaveStatus } from './autosave-status';
import { useVideoPlayer } from './player-context';
import { formatOffset } from './summary-tab';
import { useAutosave } from './use-autosave';

export function TimestampsTab({
  analysisId,
  artifact,
  onArtifactChange,
  revision,
  saveArtifact,
  durationSeconds,
  thumbnailUrl,
  sourceTitle,
  copy,
}: Readonly<{
  analysisId: string;
  artifact: TimestampsArtifact;
  onArtifactChange: (artifact: TimestampsArtifact) => void;
  revision: string;
  saveArtifact: (input: unknown) => Promise<ResultSaveState>;
  durationSeconds: number;
  thumbnailUrl: string;
  sourceTitle: string;
  copy: ResultCopy;
}>) {
  const player = useVideoPlayer();
  const value = artifact;
  const setValue = (
    update: (current: TimestampsArtifact) => TimestampsArtifact,
  ) => onArtifactChange(update(value));
  const [activeOffset, setActiveOffset] = useState(
    artifact.chapters[0]?.offsetMs ?? 0,
  );
  const save = useCallback(
    (content: TimestampsArtifact, expectedUpdatedAt: string) =>
      saveArtifact({
        analysisId,
        expectedUpdatedAt,
        kind: 'timestamps',
        content: {
          schemaVersion: 1,
          chapters: content.chapters.map(
            ({ offsetMs, title, description }) => ({
              offsetMs,
              title,
              description,
            }),
          ),
        },
      }),
    [analysisId, saveArtifact],
  );
  const autosave = useAutosave({ value, revision, save });
  useEffect(() => {
    if (!player) return;
    const synchronize = () => {
      const currentTime = player.getCurrentTimeMs();
      const active = value.chapters.findLast(
        (chapter) => chapter.offsetMs <= currentTime,
      );
      if (active) setActiveOffset(active.offsetMs);
    };
    synchronize();
    const interval = window.setInterval(synchronize, 500);
    return () => window.clearInterval(interval);
  }, [player, value.chapters]);

  const playMoment = (offsetMs: number) => {
    setActiveOffset(offsetMs);
    player?.seekTo(offsetMs);
    player?.play();
  };

  return (
    <section className="result-timestamps" data-artifact="timestamps">
      <header className="result-timeline-head">
        <h2>{formatKeyMomentsCount(copy, value.chapters.length)}</h2>
        <span>
          {copy.timestampsAutoGenerated} <span aria-hidden="true">✦</span>
        </span>
      </header>
      <AutosaveStatus {...autosave} copy={copy} />
      <ol className="result-timeline">
        {value.chapters.map((chapter, chapterIndex) => {
          const nextOffset = value.chapters[chapterIndex + 1]?.offsetMs;
          const durationMs = Math.max(
            0,
            (nextOffset ?? durationSeconds * 1_000) - chapter.offsetMs,
          );
          const current = activeOffset === chapter.offsetMs;
          return (
            <li
              key={chapter.offsetMs}
              aria-current={current ? 'true' : undefined}
              className="result-moment"
            >
              <button
                type="button"
                className="result-moment-thumb"
                aria-label={`${copy.timestampsSeek}: ${chapter.title}`}
                onClick={() => playMoment(chapter.offsetMs)}
              >
                <Image
                  src={thumbnailUrl}
                  alt={formatResultCopy(copy.sourceThumbnail, {
                    title: `${sourceTitle} — ${chapter.title}`,
                  })}
                  fill
                  sizes="104px"
                  unoptimized
                />
              </button>
              <div className="result-moment-copy">
                <button
                  type="button"
                  className="result-moment-time"
                  title={copy.timestampsSeek}
                  onClick={() => playMoment(chapter.offsetMs)}
                >
                  {formatOffset(chapter.offsetMs)}
                </button>
                <input
                  aria-label={formatResultCopy(copy.timestampsTitleField, {
                    count: chapterIndex + 1,
                  })}
                  value={chapter.title}
                  onChange={(event) =>
                    setValue((currentValue) => ({
                      ...currentValue,
                      chapters: currentValue.chapters.map((item, itemIndex) =>
                        itemIndex === chapterIndex
                          ? { ...item, title: event.target.value }
                          : item,
                      ),
                    }))
                  }
                />
                <textarea
                  aria-label={formatResultCopy(
                    copy.timestampsDescriptionField,
                    { count: chapterIndex + 1 },
                  )}
                  value={chapter.description}
                  rows={2}
                  onChange={(event) =>
                    setValue((currentValue) => ({
                      ...currentValue,
                      chapters: currentValue.chapters.map((item, itemIndex) =>
                        itemIndex === chapterIndex
                          ? { ...item, description: event.target.value }
                          : item,
                      ),
                    }))
                  }
                />
              </div>
              <span className="result-moment-duration">
                {formatOffset(durationMs)}
              </span>
              {current ? (
                <span className="sr-only">{copy.timestampsCurrent}</span>
              ) : null}
            </li>
          );
        })}
      </ol>
    </section>
  );
}
